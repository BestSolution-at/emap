CREATE TABLE ATTACHMENT
(
	SID_ATTACHMENT int64 PRIMARY KEY
	, BINARY blob segment size 2048
	, DESCRIPTION varchar(255)
	, MIMETYPE varchar(255)
	, TITLE varchar(255)

);


CREATE TABLE CHANGE_SET
(
	SID_CHANGE_SET int64 PRIMARY KEY
	, COMMENT varchar(255)
	, ID varchar(255)
	,  FK_PERSON int64 NOT NULL
	,  FK_SOURCE_REPOSITORY int64 NOT NULL
	,  FK_TASK int64 NOT NULL

);


CREATE TABLE COMMENT
(
	SID_COMMENT int64 PRIMARY KEY
	, DESCRIPTION varchar(255)
	, TITLE varchar(255)
	,  FK_TASK int64 NOT NULL

);


CREATE TABLE COMMENT_ATTACHMENT
(
	SID_COMMENT_ATTACHMENT int64 PRIMARY KEY
	,  FK_COMMENT int64 NOT NULL

);


CREATE TABLE PERSON
(
	SID_PERSON int64 PRIMARY KEY
	, FIRSTNAME varchar(255)
	, LASTNAME varchar(255)
	,  FK_REPOSITORY int64 NOT NULL

);


CREATE TABLE SOURCE_REPOSITORY
(
	SID_SOURCE_REPOSITORY int64 PRIMARY KEY
	, SOURCEURL varchar(255)
	, TYPE varchar(255)
	, WEBURL varchar(255)
	,  FK_REPOSITORY int64 NOT NULL

);


CREATE TABLE TASK
(
	SID_TASK int64 PRIMARY KEY
	, DESCRIPTION varchar(255)
	, STATE varchar(255)
	, TITLE varchar(255)
	,  FK_ASSIGNEE int64 NOT NULL
	,  FK_PRODUCT_VERSION int64 NOT NULL
	,  FK_REPORTER int64 NOT NULL
	,  FK_REPOSITORY int64 NOT NULL
	,  FK_TARGET_VERSION int64 NOT NULL

);


CREATE TABLE TASK_ATTACHMENT
(
	SID_TASK_ATTACHMENT int64 PRIMARY KEY
	,  FK_TASK int64 NOT NULL

);


CREATE TABLE TASK_REPOSITORY
(
	SID_TASK_REPOSITORY int64 PRIMARY KEY
	, DESCRIPTION varchar(255)
	, NAME varchar(255)

);


CREATE TABLE VERSION
(
	SID_VERSION int64 PRIMARY KEY
	, CODENAME varchar(255)
	, DATE timestamp
	, VALUE varchar(255)
	,  FK_REPOSITORY int64 NOT NULL

);



